function topicIndex() {
  const sections = numberedCategories
    .map((category) => {
      const topicRows = topicsFor(category)
        .map((topic) => {
          const subtopics = subtopicsFor(category, topic)
            .map((subtopic) => `  - ${docsLink(`${category}/${topic}/${subtopic}`, subtopic)}`)
            .join("\n");
          return `### ${docsLink(`${category}/${topic}`, topic)}

${subtopics}`;
        })
        .join("\n\n");

      return `## ${category}

${topicRows}`;
    })
    .join("\n\n---\n\n");

  const extraSections = extraCategories
    .map((category) => {
      const topicRows = topicsFor(category)
        .map((topic) => {
          const subtopics = subtopicsFor(category, topic)
            .map((subtopic) => `  - ${docsLink(`${category}/${topic}/${subtopic}`, subtopic)}`)
            .join("\n");
          return `### ${docsLink(`${category}/${topic}`, topic)}

${subtopics}`;
        })
        .join("\n\n");

      return `## ${category}

${topicRows}`;
    })
    .join("\n\n---\n\n");

  return `# Topic Index

This file is generated from the repository folder structure.

## Numbered Core Categories

${sections}

---

## Senior Add-On Categories

${extraSections}
`;
}

