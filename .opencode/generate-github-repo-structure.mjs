const numberedCategories = readdirSync(root)
  .filter((name) => /^\d{3}\.\s/.test(name) && statSync(join(root, name)).isDirectory())
  .sort((a, b) => Number(a.slice(0, 3)) - Number(b.slice(0, 3)));

const extraCategories = extraDirectories.filter((name) => {
  const path = join(root, name);
  return statSync(path, { throwIfNoEntry: false })?.isDirectory();
});

function link(path, label = path) {
  return `[${label}](<./${path}>)`;
}

function docsLink(path, label = path) {
  return `[${label}](<../${path}>)`;
}

function categoryTitle(directory) {
  return directory.replace(/^\d{3}\.\s/, "");
}

function topicsFor(categoryDirectory) {
  return readdirSync(join(root, categoryDirectory))
    .filter((name) => statSync(join(root, categoryDirectory, name)).isDirectory())
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));
}

function subtopicsFor(categoryDirectory, topicDirectory) {
  return readdirSync(join(root, categoryDirectory, topicDirectory))
    .filter((name) => statSync(join(root, categoryDirectory, topicDirectory, name)).isDirectory())
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));
}

function numberedTable() {
  return numberedCategories
    .map((directory) => {
      const topics = topicsFor(directory).length;
      const subtopics = topicsFor(directory).reduce((count, topic) => count + subtopicsFor(directory, topic).length, 0);
      return `| ${directory.slice(0, 3)} | ${link(directory, categoryTitle(directory))} | ${topics} | ${subtopics} |`;
    })
    .join("\n");
}

function extraTable() {
  return extraCategories
    .map((directory) => {
      const topics = topicsFor(directory).length;
      const subtopics = topicsFor(directory).reduce((count, topic) => count + subtopicsFor(directory, topic).length, 0);
      return `| ${link(directory)} | ${topics} | ${subtopics} |`;
    })
    .join("\n");
}

